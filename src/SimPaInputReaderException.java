
public class SimPaInputReaderException extends Exception {

	private static final long serialVersionUID = -2695877383196573975L;

	public SimPaInputReaderException() {
		super();
	}

	public SimPaInputReaderException(String message, Throwable cause) {
		super(message, cause);
	}

	public SimPaInputReaderException(String message) {
		super(message);
	}

	public SimPaInputReaderException(Throwable cause) {
		super(cause);
	}

	
}
